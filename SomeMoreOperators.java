
public class SomeMoreOperators {

	public static void main(String[] args) {
		int i=10;
		System.out.println(i++);//10 i=11
		System.out.println(i);//11
		System.out.println(i);//11
		System.out.println(i++);//11
		System.out.println(i);//12
		
		int j=20;
		System.out.println(++j);//21
		System.out.println(j);//21
		
		
		
	}

}
