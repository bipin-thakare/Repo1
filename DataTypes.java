
public class DataTypes {

	public static void main(String[] args) {
	
		//int :- numbers 
		int a;
		a=11;
		int b=22;
		System.out.println(a);
		System.out.println(b);
		System.out.println("b");
		
		//long :- numbers but of large size 
		long c=76765454;
		System.out.println(c);
		long d=78;
		System.out.println(d);
		
		//double :- decimal numbers 
		double e=77.897;
		System.out.println(e);
		double f=99;
		System.out.println(f);
		
		//char :- alphabet, special char, number as char, space
		char h='x';
		char i='#';
		char j='6';
		char k=' ';
		System.out.println(h);
		System.out.println(j);
		
		//boolean :- true or false
		boolean m=true;
		System.out.println(m);
		boolean n=false;
		System.out.println(n);
		
		//String :- it can literally store anything
		String str="   abc xyz   ";
		System.out.println(str);
		
		String str1="This   is the way we store string variable";
		System.out.println(str1.length());
		
		String str2="learning more on string data type";
		System.out.println(str2.toUpperCase());	
		

	}

}
